$(document).ready(function () {

    $('#tabla_empresas').DataTable({
        columnDefs: [{
                orderable: false,
                className: 'select-checkbox',
                targets: 0
            }],
        select: {
            style: 'os',
            selector: 'td:first-child'
        },
        order: [[5, 'desc']],
        "language": {
            "sProcessing": "Procesando...",
            "sLengthMenu": "Mostrar _MENU_ registros",
            "sZeroRecords": "No se encontraron resultados",
            "sEmptyTable": "Ningún dato disponible en esta tabla",
            "sInfo": "Mostrando registros del _START_ al _END_ de un total de _TOTAL_ registros",
            "sInfoEmpty": "Mostrando registros del 0 al 0 de un total de 0 registros",
            "sInfoFiltered": "(filtrado de un total de _MAX_ registros)",
            "sInfoPostFix": "",
            "sSearch": "Buscar:",
            "sUrl": "",
            "sInfoThousands": ",",
            "sLoadingRecords": "Cargando...",
            "oPaginate": {
                "sFirst": "Primero",
                "sLast": "Último",
                "sNext": "Siguiente",
                "sPrevious": "Anterior"
            },
            "oAria": {
                "sSortAscending": ": Activar para ordenar la columna de manera ascendente",
                "sSortDescending": ": Activar para ordenar la columna de manera descendente"
            }
        },
        "autoWidth": false,
        responsive: true

    }).on('select', function (e, dt, type, indexes) {
        if (type === 'row') {
            var data = $('#tabla_empresas').DataTable().row(indexes).data();
            //alert(data[1]); 
            var input = document.getElementById("id_empresa");
            input.setAttribute("value", data[1]);
            //alert(input.value);

            $('#datos_proyecto').attr('hidden', false);
        }
    }).on('deselect', function (e, dt, type, indexes) {

        //var data = $('#tabla_empresas').DataTable().row(indexes).data();
        //alert(data[1]); 
        var input = document.getElementById("id_empresa");
        input.setAttribute("value", '');
        //alert(input.value);

        $('#datos_proyecto').attr('hidden', true);

    });
});

